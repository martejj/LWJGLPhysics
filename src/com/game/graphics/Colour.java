package com.game.graphics;

public class Colour {

    float red;
    float green;
    float blue;

    public Colour(float red, float green, float blue) {

        this.red = red;
        this.blue = blue;
        this.green = green;

    }

    public Colour() {

        red = 0;
        green = 0;
        blue = 0;

    }

}
